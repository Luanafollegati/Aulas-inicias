let primeiroNumero = 20;
let segundoNumero = 30;
let operacao = "*";

switch (operacao) {
  case "+":
    console.log(`O resultado da soma é de ${primeiroNumero + segundoNumero}`);
    break;

  case "-":
    console.log(
      `O resultado da subtraçao é de ${primeiroNumero - segundoNumero}`
    );
    break;

  case "*":
    console.log(`O resultado da  é de ${primeiroNumero * segundoNumero}`);
    break;

  case "/":
    console.log(
      `O resultado da divisão é de ${primeiroNumero / segundoNumero}`
    );
    break;

  default:
    console.log("Operação invalida");
    break;
}


